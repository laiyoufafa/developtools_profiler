package ohos.devtools.views.distributed.bean;

import ohos.devtools.views.trace.DField;

/**
 * DistributedThreadBean
 *
 * @since 2021/08/11 20:13
 */
public class DistributedThreadBean {
    @DField(name = "id")
    private Integer id;

    @DField(name = "name")
    private String name;

    @DField(name = "tid")
    private Integer tid;

    @DField(name = "pid")
    private Integer pid;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Integer getTid() {
        return tid;
    }

    public void setTid(Integer tid) {
        this.tid = tid;
    }

    public Integer getPid() {
        return pid;
    }

    public void setPid(Integer pid) {
        this.pid = pid;
    }
}
