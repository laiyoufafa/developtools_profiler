package ohos.devtools.datasources.utils.device.entity;

import ohos.devtools.views.common.LayoutConstants;

import java.io.Serializable;
import java.util.Objects;

/**
 * Device IP and port number information
 */
public class DeviceIPPortInfo implements Serializable {
    private static final long serialVersionUID = -290179609047525076L;
    private String deviceID;
    private String deviceName;
    private String ip;
    private int port;
    private DeviceType deviceType;
    private String connectType = LayoutConstants.USB;
    private int forwardPort;
    private int deviceStatus;
    private int retryNum;
    private int offlineCount;

    public DeviceType getDeviceType() {
        return deviceType;
    }

    public void setDeviceType(DeviceType deviceType) {
        this.deviceType = deviceType;
    }

    public String getDeviceName() {
        return deviceName;
    }

    public void setDeviceName(String deviceName) {
        this.deviceName = deviceName;
    }

    public String getDeviceID() {
        return deviceID;
    }

    public void setDeviceID(String deviceID) {
        this.deviceID = deviceID;
    }

    public String getIp() {
        return ip;
    }

    public void setIp(String ip) {
        this.ip = ip;
    }

    public int getPort() {
        return port;
    }

    public void setPort(int port) {
        this.port = port;
    }

    public int getForwardPort() {
        return forwardPort;
    }

    public void setForwardPort(int forwardPort) {
        this.forwardPort = forwardPort;
    }

    public String getConnectType() {
        return connectType;
    }

    public void setConnectType(String connectType) {
        this.connectType = connectType;
    }

    public int getDeviceStatus() {
        return deviceStatus;
    }

    public void setDeviceStatus(int deviceStatus) {
        this.deviceStatus = deviceStatus;
    }

    public int getRetryNum() {
        return retryNum;
    }

    public void setRetryNum(int retryNum) {
        this.retryNum = retryNum;
    }

    public int getOfflineCount() {
        return offlineCount;
    }

    public void setOfflineCount(int offlineCount) {
        this.offlineCount = offlineCount;
    }

    @Override
    public boolean equals(Object obj) {
        return super.equals(obj);
    }

    @Override
    protected Object clone() throws CloneNotSupportedException {
        return super.clone();
    }

    @Override
    public int hashCode() {
        return Objects
            .hash(deviceID, deviceName, ip, port, deviceType, connectType, forwardPort, deviceStatus, retryNum);
    }

    @Override
    public String toString() {
        return deviceName;
    }
}
